#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	int	i;
	int	p;

	if (s != NULL && f != NULL)
	{
		i = 0;
		p = ft_strlen(s);
		while (i < p)
		{
			(*f)(s);
			s++;
			i++;
		}
	}
}
